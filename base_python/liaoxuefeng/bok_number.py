# Python使用两个**代表乘方运算
power = 2 ** 3
print(power)
power = 3 / 2
print(power)
power = 2 + 3 * 4
print(power)
power = (2 + 3) * 4
print(power)

# 浮点数
# 结果包含的小数位数可能是不确定的
add = 0.1 + 0.2
print(add)
minus = 0.2 - 0.1
print(minus)
multiply = 0.1 * 0.2
print(multiply)
divide = 0.15 / 0.2
print(divide)

